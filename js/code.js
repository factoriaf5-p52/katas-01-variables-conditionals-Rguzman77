function evenOrOdd(n) {
    if (n % 2 === 0)
    return "even";
    else return "odd";
}

function greeting(name) {
    if (name = "Javi") {
        greeting = "Hola, Javi!";
      } else if (name = "gente"){
        greeting = "Hola, gente!";
      }  else if (name = "Mary"){
        greeting = "Hola, Mary!";
      }
}
