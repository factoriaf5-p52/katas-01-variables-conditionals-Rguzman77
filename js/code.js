function evenOrOdd(n) {
    if (n % 2 === 0)
    return "even";
    else return "odd";
}

function greeting(name) {
    if (name === "Javi") {
        return "Hola, Javi!";
      } else if (name === "gente"){
        return "Hola, gente!";
      }  else if (name === "Mary"){
        return "Hola, Mary!";
      }
}
